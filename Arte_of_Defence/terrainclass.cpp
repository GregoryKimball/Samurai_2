bool TerrainClass::Initialize(ID3D11Device* device, char* heightMapFilename, WCHAR* textureFilename)
{
	bool result;


	// Manually set the width and height of the terrain.
	//m_terrainWidth = 100;
	//m_terrainHeight = 100;



	// Load in the height map for the terrain.
	result = LoadHeightMap(heightMapFilename);
	//result = GenerateHeightMap();
	if(!result) 	{ 		return false;	}

	result = RoughenHeightMap();
	//result = GenerateHeightMap();
	if(!result) 	{ 		return false;	}

	// Normalize the height of the height map.
	NormalizeHeightMap();


	// Calculate the normals for the terrain data.
	result = CalculateNormals();
	if(!result)
	{
		return false;
	}


	//We now generate our own terrain texture coordinates for the terrain grid during the Initialize function.

	// Calculate the texture coordinates.
	CalculateTextureCoordinates();
//The texture that will be used for rendering the terrain is also loaded here in the Initialize function.

	// Load the texture.
	result = LoadTexture(device, textureFilename);
	if(!result)
	{
		return false;
	}


	// Initialize the vertex and index buffer that hold the geometry for the terrain.
	result = InitializeBuffers(device);
	if(!result)
	{
		return false;
	}

	return true;
}



void TerrainClass::Shutdown()
{

	// Release the texture.
	ReleaseTexture();

	// Release the vertex and index buffer.
	ShutdownBuffers();

	// Release the height map data.
	ShutdownHeightMap();

	return;
}

